Neue Kennung für das 70cm Relais Oldenburg (Amateurfunk)</p>

<p>Hallo Funkfreunde,<br>
der eine oder andere der Telegraphie mächtige und / oder geübte Zuhörer wird sicher schon festgestellt haben, das die Kennung des 70cm Relais von Oldenburg auf der 438.650 von DB0OO in DB0UO geändert wurde. Mit Ablauf der letzten Genehmigungsperiode habe ich aus verwaltungs -und kostentechnischen Gründen die Zusammenlegung der in Wahnbek befindlichen Relaisstellen unter einem Call beantragt. DB0OO ist somit Geschichte!</p>

<p>73, de Klaus, DL8BDU</p>


<h2><p>Thema: aktuelle News 28.9.2013 2m Relais DB0UO abgeschaltet</p></h2>

<p>Hallo Funkfreunde,<br>
gestern habe DF7BW, DL3IKE und ich in Wahnbek die jährliche Inspektion der Relaisfunkstelle DB0UO durchgeführt. Weil im Vorfeld schon der Hinweis auf zeitweise Beeinträchtigungen des 2m Relais laut wurden hat Eike die ihm zur Verfügung stehende kommerzielle Meßtechnik vor dem Aufstieg zur Antenne eingesetzt und beide Kabel in die Turmspitze mit den angeschlossenen Antennen gemessen. Der 70cm Bereich entsprach voll unseren Erwartungen. Auf 2m zeigten sich jedoch keine definierten Zustände, so das, obwohl Funkbetrieb relativ gut möglich war, ein Fehler vorliegen musste. Mit entsprechend Werkzeug und Meßtechnik ausgestattet sind wir dann aufgestiegen. Wir vermuteten eine Beschädigung der Antennenzuführung, da auf der oberen Plattform vor kurzem Sanierungsarbeiten durchgeführt wurden. Für eine 2. Messung im Turmkopf habe ich dann die Zuleitung zur Antenne (1/2″ CF) am Transformationsglied abgeschaubt und…..hatte nasse Finger. Wasser im Kabel, bei Cellflex? Also sind wir auf die Spitze und haben die Antennenleitung verfolgt bis zur Antenne, kein Schaden feststellbar. Ein Blick an der Antenne entlang gen Spitze zeigte dann den schon inzwischen befürchteten Grund für alles Übel….siehe Bilder hierzu. Wir haben Antenne und Kabel dann abgebaut und ich werde die Tage nachsehen, ob an der Antenne nach dem Blitzeinschlag noch etwas zu retten ist oder nicht. So bleibt also bis auf weiteres das 2m Relais DB0UO abgeschaltet.
Bilder bei Klaus auf der website
</p>

<p>73, de Klaus, DL8BDU</p>

