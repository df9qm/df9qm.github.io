<h2>2016 - Rückbau</h2>
<h3>Aufgabe und Rückbau von DB0UO in Wahnbek </h3>
<p>Geschrieben von Klaus Mueller am Montag, 11. April 2016</p>
<p>Hallo Funkfreunde!<br>
Auf Grund geänderter vertraglicher Bedingungen müssen wir den Standort der beiden Relais DB0UO auf 2m und 70cm in Wahnbek kurzfristig aufgeben und die komplette Anlage demontieren. Da die Lizenz Ende des Monats ausläuft ist das auch der Termin für die endgültige Abschaltung. Ob ein Betrieb an anderer Stelle möglich sein wird ist zur Zeit in Prüfung, aber definitiv nicht spruchreif.</p>

<p>73, de Klaus, DL8BDU</p>

