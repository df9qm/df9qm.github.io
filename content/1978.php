<h2>1978 - DB0UO</h2>

<p>Horst und Manfred kamen auf die Idee, eine Horizontalantenne für DB0UO zu bauen. Klaus und Reinhard, DF4BT, zogen sie außen hoch, und bauten sie zusammen. Ich kam zu spät und konnte daher nur einige Photos von unten machen(mit 400mm Tele) Klaus DK7BS und Reinhard, DF4BT. Erstmal mußte an dem morschen Holz ein Rohr befestigt werden. Das war 1978. Ziel war eine Verbesserung für Feststationen. Die Horizontalrundstrahler hatte Horst, DJ3PI, konstruiert. Zusammen haben sie 3dBD.</p>
