    <h2 id="relaisdaten">Die Daten der 2m Relaisstelle DB0UO auf einen Blick</h2>
    
    <p>Bei der Reichweitenberechnung wurde die Sendeleistung mit 15W eingesetzt , die Spannung am Empfänger mit 3 Mikrovolt angenommen. Sind 2 Werte vorhanden, gilt der erste für die Sichtweite und der zweite für die mögliche 3 µV Reichweite hinter dem optischen Horizont. Die Reichweite wurde für Mobilstationen berechnet mit einer Antennenhöhe von 1m und 0dB. 3 µV dürfte ein angemessener Wert für Funkamateure sein, sie können ja im Rauschen noch hören. Bei kommerziellen Diensten wird mit 20 Mikrovolt gerechnet! Höhenangaben in Meter über NN.</p>
    
