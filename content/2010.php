Hallo Funkfreunde,<br>
Klaus und ich waren heute noch einmal in Wahnbek und haben am 70cm Relais einige Veränderungen vorgenommen. Wir hoffen nun auf ein Ausbleiben der Modulationsaussetzer, der Hub des Tonrufes wurde auch angepasst und einige andere Dinge, jedoch nicht die Ausgangsleistung! Sollten Änderungen negativer oder positiver Natur zu beobachten sein bitte ich um kurze Nachricht! Des weiteren habe ich eine Erweiterung der Betriebsarten für das 2m Relais bei der Bundesnetzagentur beantragt. Wenn der Bescheid positiv ist werden wir im März einen Testbetrieb in F3E und F1D (D-Star) im Parallelbetrieb durchführen. Es gibt vorerst keine Anbindung an das Internet!!!! Leider kam es in Wahnbek, wie auch in der Zeitung zu lesen war, zu einem Zwischenfall mit der Steuerung der Weihnachtsbeleuchtung. Hier hatte wohl ein Bauteil geschmort und Brandalarm ausgelöst. Ich hoffe sehr das es keine negativen Auswirkungen auf die Meinungsbildung der Turmbetreiber zu Selbstbauanlagen geben wird!! Das würde uns, nachdem sich die Lage leicht entspannt hat, gerade noch fehlen. Wie ich heute noch aus berufenem Munde erfahren musste geht der Abbau von Amateurfunkanlagen an dieser Art von Standorten unvermindert weiter. na denn…</p>

<p>73, de Klaus, DL8BDU 6.1.2010</p>

<h3><p>DB0UO und D-Star Geschrieben von Klaus Mueller am Sonntag, 14. März 2010</p></h3>

<p>Hallo Funkfreunde,
nachdem die Genehmigung für das 2m Oldenburg Relais zum gleichzeitigen Betrieb zweier Betriebsarten
eingetroffen war und Klaus aus dem Urlaub aus EA8 zurück ist stand der weiteren Planung und Vorbereitung
zur Inbetriebnahme von D-Star am Standort Oldenburg eigentlich nichts mehr im Wege…. doch weit gefehlt.</p>

<p>Derzeit wird der „sinnvolle“ Betrieb eines D-Star Relais auf Grund der Querelen um das D-STAR-Gateway System, also um die Anmeldung am Server in den USA, welcher von den Eignern für nicht ICOM Nutzer ausgeschlossen wird, nicht möglich. Klaus versucht eine Lösung zu finden…aber ich befürchte, da hier Kommerz gegen Amateur kämft, das Ergebnis ist schon eingetütet!</p>

<p>Sollten sich Änderungen in der einen oder anderen Richtung ergeben werde ich sie selbstverständlich hier
veröffentlichen!</p>

<p>73, de Klaus, DL8BDU</p>

